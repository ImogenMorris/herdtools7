(** (Relative) branch argument *)

type t = Lab of Label.t | Off of int

let pp = function
| Lab lbl -> Label.pp lbl
| Off n ->
   if n < 0 then Printf.sprintf ".%d" n
   else Printf.sprintf ".+%d" n

let toNext =
  let open Label in
  function
  | Lab l -> To l
  | Off n -> Disp n

